// Programa 2.5 - Concatenación de textos
// Pág. 45 - Uso del operador + para unir cadenas de texto

class Concatenacion {
    public static void main(String[] args) {
        String nombre           = "John";
        String primerApellido   = "Doe";
        String nombreConApellido = nombre + " " + primerApellido;

        System.out.println(nombreConApellido);
    } // Fin del método main
} // Fin de la clase Concatenacion